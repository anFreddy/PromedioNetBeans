    private void cb_GuiaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_GuiaItemStateChanged
        // TODO add your handling code here:
        
        if (evt.getStateChange() == ItemEvent.SELECTED){
            String opcion = cb_Guia.getSelectedItem().toString();
                    if ("40%".equals(opcion)){
                    txt_Control_Lectura.setText("");
                    txt_Control_Lectura.setEnabled(false);
                    cb_Control_Lectura.setEnabled(false);
                    cb_Control_Lectura.setSelectedItem("0%");
                    }
                    else{
                        txt_Control_Lectura.setEnabled(true);
                        cb_Control_Lectura.setEnabled(true);
                        cb_Control_Lectura.setSelectedItem("20%");
                    }
       }
    }//GEN-LAST:event_cb_GuiaItemStateChanged

    private void btn_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CalcularActionPerformed
        // TODO add your handling code here:
        
        if (ValidarControles()){
            String estudiante = "";
            double parcial = 0.0;
            double guia = 0.0;
            double lectura = 0.0;
            Integer porcentaje_guia = 0;
            Integer porcentaje_lectura = 0;
            
            estudiante = txt_Nombre.getText();
            
            try{
                parcial = Double.parseDouble(txt_Parcial.getText());
                guia = Double.parseDouble(txt_Guia.getText());

                if (txt_Control_Lectura.isEnabled()){
                    lectura = Double.parseDouble(txt_Control_Lectura.getText());
                    porcentaje_lectura = Integer.parseInt(cb_Control_Lectura.getSelectedItem().toString().replace("%", ""));
                }                
                
                porcentaje_guia = Integer.parseInt(cb_Guia.getSelectedItem().toString().replace("%", ""));
                
                double r_parcial = parcial * 0.60;
                double r_guia = guia * porcentaje_guia/100;
                double r_lectura = lectura * porcentaje_lectura/100;
                
                double promedio = r_parcial + r_guia + r_lectura;
                                
                JOptionPane.showMessageDialog(null, "Promedio: "+promedio+"\n"+estudiante);
            }catch(Exception ex) {
                JOptionPane.showMessageDialog(null, "Error en los campos de notas");
            }
        }
    }//GEN-LAST:event_btn_CalcularActionPerformed

