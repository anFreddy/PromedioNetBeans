    public void Cargar_Porcentajes(){
         ArrayList<Integer> guia = new ArrayList<>();
         guia.add(40); guia.add(20);
         
         ArrayList<Integer> lectura = new ArrayList<>();
         lectura.add(20); lectura.add(0);
         
         for( Integer item : guia ){
             cb_Guia.addItem(item+"%");
         }
         
         for ( Integer item : lectura){
             cb_Control_Lectura.addItem(item+"%");
         }
         
         cb_Control_Lectura.setSelectedItem("0%");
    }
    
    public boolean ValidarControles(){
        
        if (txt_Nombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar nombre del estudiante");
            return false;
        }
        
        if (txt_Parcial.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar nota del parcial");
            return false;
        }
        
        if (txt_Guia.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar nota de la guía");
            return false;
        }
        
        if (txt_Control_Lectura.isEnabled() && txt_Control_Lectura.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar nota del control de lectura");
            return false;
        }
        
        return true;
    }

